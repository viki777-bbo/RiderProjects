class Book
{
    public string name;
    public string author;
    public int releaseYear;
    public int pageCount;
    
    public Book(string name, string author, int releaseYear, int pageCount)
    {
        this.name = name;
        this.author = author;
        this.releaseYear = releaseYear;
        this.pageCount = pageCount;
    }
    